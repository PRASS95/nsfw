/**
 * 
 * Animated View
 *
 * v2.0
 */

class AnimatedView extends SmartView {

	constructor(id, view, options = {}) {

		super(id, view, options);
	}

	/**
	 * Update
	 */
	update( status ) {

		super.update( status ); // (see SmartView)

		if( this.visibility && !this.visible )
			this.display();
		else if( !this.visibility && this.visible )
			this.hide();

	}

	/**
	 * Display
	 * Add your own animation (override)
	 */
	display() {

		this.visible = true;			
	}

	/**
	 * Hide
	 * Add your own animation (override)
	 */
	hide () {

		this.visible = false;
	}
}

export default AnimatedView;